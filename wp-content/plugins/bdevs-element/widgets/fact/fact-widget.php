	protected function render() {
        $settings = $this->get_settings_for_display();

        $this->add_inline_editing_attributes( 'title', 'basic' );
        $this->add_render_attribute( 'title_2', 'class', 'section-title white-color' );
        $this->add_render_attribute( 'title', 'class', 'title bdevs-el-title' );

        // button
        if (!empty($settings['button_link'])) {
            $this->add_inline_editing_attributes( 'button_text', 'none' );
            $this->add_render_attribute( 'button_text', 'class', 'bdevs-btn-text' );
            $this->add_render_attribute( 'button', 'class', 'bdevs-btn bt-btn' );
            $this->add_link_attributes( 'button', $settings['button_link'] );
        }

        $this->add_inline_editing_attributes( 'description', 'intermediate' );
        $this->add_render_attribute( 'description', 'class', 'bdevs-card-text' );

        if ( empty( $settings['slides'] ) ) {
            return;
        }
        ?>

        <?php if ( $settings['design_style'] === 'style_1' ): ?>
        <section class="counter_section_1">
            <div class="container">
            <?php if(!empty($settings['slides'])) : ?>
              <div class="counter_items_wrap">
                <?php foreach ( $settings['slides'] as $key => $slide ) : ?>
                <div class="counter_item">
                  <div class="item_icon elementor-repeater-item-<?php echo esc_attr( $slide['_id'] ); ?>">
                     <?php if ( $slide['type'] === 'image' && ( $slide['image']['url'] || $slide['image']['id'] ) ) :
                        $this->get_render_attribute_string( 'image' );
                        $slide['hover_animation'] = 'disable-animation'; // hack to prevent image hover animation
                        ?>
                            <?php echo Group_Control_Image_Size::get_attachment_image_html( $slide, 'thumbnail', 'image' ); ?>
                            <?php elseif ( ! empty( $slide['icon'] ) || ! empty( $slide['selected_icon']['value'] ) ) : ?>
                            <?php bdevs_element_render_icon( $slide, 'icon', 'selected_icon' ); ?>
                    <?php endif; ?>
                  </div>
                  <?php if(!empty($slide['fact_number'])) : ?>
                  <div class="counter_text  bdevs-el-title"><span class="counter"><?php echo bdevs_element_kses_basic( $slide['fact_number'] ); ?></span>+</div>
                  <?php endif; ?>
                  <?php if ( !empty( $slide['description'] ) ) : ?>
                       <h3 class="item_title bdevs-el-subtitle"><?php echo bdevs_element_kses_basic( $slide['description'] ); ?></h3>
                   <?php endif; ?>
                </div>
                <?php endforeach; ?>
              </div>
              <?php endif; ?>
            </div>
          </section>
        <?php elseif ( $settings['design_style'] === 'style_2' ): ?>
            <section class="counter__area counter__area-2">
                <div class="container">
                    <div class="row">
                        <?php foreach ( $settings['slides'] as $key => $slide ) : ?>
                        <div class="col-xl-3 col-lg-3 col-md-6 col-sm-6">
                            <div class="counter__item counter__item-2 text-center mb-30 bdevs-el-content">
                                <h2 class="counter bdevs-el-title elementor-repeater-item-<?php echo esc_attr($slide['_id']); ?>" ><?php echo bdevs_element_kses_basic( $slide['number'] ); ?></h2>
                                <?php if ( !empty( $slide['subtitle'] ) ) : ?>
                                <span class="bdevs-el-subtitle"><?php echo bdevs_element_kses_basic( $slide['subtitle'] ); ?></span>
                                <?php endif; ?>
                                <?php if ( !empty( $slide['description'] ) ) : ?>
                                <p class="mt-15"><?php echo bdevs_element_kses_basic( $slide['description'] ); ?></p>
                                <?php endif; ?>
                            </div>
                        </div>
                        <?php endforeach; ?>
                    </div>
                </div>
            </section>     

         <?php endif; ?>   

        <?php
    }
